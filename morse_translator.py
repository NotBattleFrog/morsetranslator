inverse_morse = {v:k for k,v in morse.items()}

def toCode(sentence) :
    encoded = ''
    up_sen = sentence.upper()
    for char in up_sen :
            encoded += morse.get(char,'<UKN>') # <UKN> = Unknown
    return encoded
          
def toWord(code) :
    decoded = ''
    splited = code.split()
    for char in splited :
            decoded += inverse_morse.get(char,'<UKN>')
    return decoded


